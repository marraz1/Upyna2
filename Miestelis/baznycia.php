<p><b>Upynos Švč. Mergelės Marijos Vardo bažnyčia</b> stovi Upynos miestelyje, 10 km į šiaurės vakarus nuo Skaudvilės. Savo forma primena tradicinį gyvenamąjį namą, turi klasicistinių elementų.
</p>

<h2><span class="mw-headline" id="Istorija">Istorija</span></h2>
<p>Upynos bažnyčia 1688 m. buvo Šilalės, XVIII a. – Kaltinėnų, nuo 1804 m. – Girdiškės parapijos filija. 1705 m. bažnyčia sudegė, 1706 m. atstatyta, 1834 m. vėl sudegė.
</p>
<p>1835–1836 m. pastatyta dabartinė medinė bažnyčia. Jai varpai atvežti iš uždarytos Raseinių karmelitų bažnyčios. 1864 m. areštuotas ir ištremtas Upynos filialistas Antanas Bogušas (mirė 1871 m. Tobolske). 1913 m. bažnyčia padidinta. 1926 m. įkurta parapija.
</p>
<h2>Vincentas Juzumas. Žemaičių vyskupija. Upyna</h2>
<p>Upynos miestelis yra Raseinių apskrityje, jis kadaise priklausė seniūnijai. Upyna yra prie Upynalės upelio bei kelio, kuris jungia Upyną su Šilale, Skaudvile, Nemakščiais, Raseiniais. Miestelis mažas, jame dvi gatvės. Vienoje iš jų, Obelyno gatvėje, gyvena savininkai katalikai, kitoje, Skaudvilės gatvėje, gyvena beveik vieni žydai. Apskritai namų šiame miestelyje apie 60 ir visi mediniai. Jomarkų nebūna, o turgus kartą per savaitę. Miestelis buvo Upynos seniūno dvaras. Šiandien jis griuvėsiuose, o žemė išdalinta valstiečiams.
</p><p>Ponai Slavočinskiai ilgą laiką ėjo Upynos seniūno pareigas, netgi ir prie dabartinės valdžios.
</p><p>Upynos pavadinimas yra kilęs nuo vandens dievo vardo Upinis, kurį iš pagoniškos pagarbos dievybei tos vietovės mišką pašventino ir pavadino Šventgiriu.
</p><p>Bažnyčia. Pradinio Upynos steigimo dokumento nėra. Iš kažkokio Kazimiero Mikuličiaus testamento sužinome, kad jis 1607&nbsp;m. sausio 25 d. užrašė Upynos bažnyčiai 3 tūkst. funtų. Jeigu data neklaidino, Upynos bažnyčios pradžia būtų 1607&nbsp;m. , tačiau sudarytame 1613&nbsp;m. bažnyčių sąraše jos nėra. Tikriausiai Upynos bažnyčios įsteigimo data yra 1689&nbsp;m. 1690&nbsp;m. oficialiame rašte, tarp kitų bažnyčių yra ir pastaroji kaip Šilalės filija. Pagal švedų karo tradiciją ši bažnyčia turėjo būti sudeginta, bet kas atstatė, nežinoma. Tikriausiai Upynos seniūnas. Žinoma, kad 1716&nbsp;m. ji jau buvo, nes tai rodo metrikacijos knygos. Upynoje buvo parapijos bažnyčia iki tol, kol įformino Girdiškės parapiją ir nuo to laiko Upyna liko filija. Ši bažnyčia statyta apie 1708&nbsp;m., restauruota 1820&nbsp;m., o 1834&nbsp;m. per miestelio gaisrą sudegė. Kitais metais po gaisro apylinkės gyventojų lėšomis ir filijos kun. Pranciškaus Stankevičiaus pastangomis bažnyčia buvo atstatyta ir ji tebėra iki šios dienos.
</p><p>Bažnyčia vadinama Panelės Marijos vardu.
</p><p>Varpinė kartu su bažnyčia sudėgė, ir varpai, kurie buvo, nukrito žemėn ir sudužo. Dabartinė varpinė pastatyta atstatant bažnyčią. Varpai, vyskupijos vadovybės nurodymu, perduoti iš Raseinių karmelitų bažnyčios.
</p><p>Yra tvarkingai pastatytas filijalisto gyvenamasis namas, bažnyčia turi valaką žemės, kuri kadaise buvo seniūno paskirta. Bažnyčios fondą sudaro 1200 auksinių, kuriuos padovanojo Požerės seniūnas Povilas Juškevičius. 1607&nbsp;m. Jurgis Mikuličius testamente užrašė bažnyčiai 3000 auksinių, kurie dingo.
</p>
<h2><span id="Architekt.C5.ABra"></span>Architektūra</h2>
<p>Bažnyčia savo forma primena tradicinį gyvenamąjį namą, stačiakampio plano, su bokšteliu. Įrengti 3 altoriai. Šventoriaus tvora akmenų mūro su vartais (statyti 1973 m., meistras Antanas Martyšius. Jame stovi maža varpinė (XIX a.).</p>
